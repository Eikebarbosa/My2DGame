    private void entrarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        System.out.println("12131");

        try {
            var login = loginTextField.getText();
            var usuario = UsuarioDAO.getByLogin(login);

            if (usuario != null) {
                JOptionPane.showMessageDialog(this, "J√° possui um login com essas credenciais!");
                return;
            }

            var senha = new String(senhaPasswordField.getPassword());

            usuario = new Usuario(login, senha, 0);

            UsuarioDAO.inserir(usuario);

            new LoginTela().setVisible(true);
            this.dispose();
        } catch (RuntimeException re) {
            re.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao tentar registrar: " + re.getMessage());
        }
    }

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {
        new LoginTela().setVisible(true);
        dispose();
    }

    private void senhaToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (senhaToggleButton.isSelected()) {
            senhaPasswordField.setEchoChar((char) 0); // Mostrar a senha
        } else {
            senhaPasswordField.setEchoChar('*'); // Ocultar a senha
        }

    }

